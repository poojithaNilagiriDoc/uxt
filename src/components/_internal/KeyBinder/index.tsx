export default React.memo(function KeyBinder(props: KeyBinderProps) {
  const { children, isDisabled = false } = props;

  const handleKeyDown = React.useCallback(
    function handleKeyDown(e) {
      if (isDisabled) return;

      e.persist();

      const accessor = `on${getPrefix(e)}${upperFirst(
        keyCodes[e.keyCode],
      )}KeyDown`;
      const handler = safeGet(noop, accessor, props);

      handler(e);
    },
    [isDisabled, props],
  );

  const handleKeyUp = React.useCallback(
    function handleKeyUp(e) {
      if (isDisabled) return;

      e.persist();

      const accessor = `on${getPrefix(e)}${upperFirst(
        keyCodes[e.keyCode],
      )}KeyUp`;
      const handler = safeGet(noop, accessor, props);

      handler(e);
    },
    [isDisabled, props],
  );

  const child = React.Children.only(children);

  return React.cloneElement(child, {
    onKeyDown: handleKeyDown,
    onKeyUp: handleKeyUp,
  });
});

function getPrefix(e) {
  const ctrl = e.ctrlKey ? 'Ctrl' : '';
  const shift = e.shiftKey ? 'Shift' : '';
  const alt = e.altKey ? 'Alt' : '';
  return `${ctrl}${shift}${alt}`;
}
